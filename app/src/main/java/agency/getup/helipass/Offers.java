public class Offers {

        private int id;

        private float price;

        private String name;

        private float latitude;

        private float longitude;


        public int getId() {
            return id;
        }

        public float getPrice() {
            return price;
        }

        public String getName() {
            return name;
        }

        public float getLatitude() {
            return latitude;
        }

        public float getLongitude() {
            return longitude;
        }
}
